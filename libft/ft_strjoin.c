#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*result;
	size_t	len1;
	size_t	len2;
	size_t	idx;

	idx = 0;
	len1 = ft_strlen(s1);
	len2 = ft_strlen(s2);
	result = (char *)malloc(len1 + len2 + 1);
	if (!result)
		return (0);
	ft_strlcat(result, s1, len1 + 1);
	ft_strlcat(result + len1, s2, len2 + 1);
	return (result);
}
